          <div class="item-hd color-a"><h3>The Selection Process/Adjudication Procedures</h3></div>
          <div class="item-bd">
            <p><i>All applicants, whether accepted, not accepted, or accepted as an alternate, will be notified by email in mid-April.</i></p>
            <p>Auditions are judged by professional artists, educators, and members of the Oklahoma Arts Institute Artistic Advisory Panel. Three judges adjudicate each discipline/instrument, and students are selected based on an average of the three judges’ scores.</p>
            <p>Acting, ballet, and modern dance auditions are all “live” auditions, meaning applicants are scored at the audition. Choir and orchestra auditions are digitally recorded and judged at a later date. Choir, orchestra, creative writing, and drawing & painting auditions are blind, meaning adjudicators do not know the applicant’s name, age, hometown, etc. Film & video and photography auditions are partially blind, in that the interviewer has information about the applicant, but the other adjudicators who score the work at a later date do not. Sample adjudication forms for each artistic discipline are located on each discipline’s page.</p>
            <p>Please note: Pre-recorded/videotaped auditions are not permitted. We work hard to ensure the audition process is as consistent and fair as possible. In order to achieve this, we require all applicants to complete the same audition requirements. The live audition component is a vital part of the Summer Arts Institute audition process because it helps to establish each applicant’s maturity, their ability to work well under pressure, and, thus, their chances of success in the rigorous Summer Arts Institute environment.</p>
            <p>All auditions are judged on a statewide basis, NOT by audition location. Selection is made at the conclusion of statewide auditions based on audition scores, NOT on audition location, applicants’ hometowns, or other demographic information. Whether a student chooses to audition in his or her own town or another location will have ABSOLUTELY NO IMPACT on that student’s chance of being accepted to OSAI.</p>
            <p>Please keep in mind that OSAI is extremely competitive. Each year, over 1,000 students audition for about 270 spots. If you aren’t accepted this year, please try again next year. Many of our alumni were not accepted the first time they auditioned. Thank you for auditioning and good luck!</p>
