          <div class="item-bd">
            <p>Archeological evidence suggests Paleo-Indian people hunted wooly mammoth near Quartz Mountain between 11,200 and 20,000 years ago.  Excavations at the Cooperton Site in Kiowa County provide the earliest known evidence of people in the state of Oklahoma.</p>
            <p>Spain and France traded Quartz Mountain several times before the U.S. bought the area as part of the Louisiana Purchase in 1803.  Several Native American tribes occupied the area until the late 1880s. In 1887, Quartz Mountain was affected by the Gold Rush, but the U.S. military regained control of the region and opened the land to white settlement in 1901.  In 1937, Quartz Mountain officially became a state park.</p>
            <p>The tiny town of Lugert was flooded to create a municipal water supply for Altus in 1927. Today, Lake Altus-Lugert also provides irrigation for 48,000 acres of southwestern Oklahoma farmland.</p>
