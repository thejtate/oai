            <p>As our country grows increasingly diverse and the internet makes music from around the world more easily accessible, many music educators are looking for ways to incorporate world music into the curriculum. However, if students are not actively engaged in music-making and do not connect the material to their contemporary lives, lessons on world music can bore children and lead to simplistic experiences with the music. In this workshop, participants will explore issues related to two types of multicultural learning experiences. 1. Listening lessons:  1. Experience characteristics of successful (and unsuccessful!) listening lessons, actively recreate music using instruments and voices, and create listening lessons of student-chosen repertoire.  2. Songs and singing games: Learn songs and singing games from many parts of the world, discuss what makes them work, and observe videos of elementary-aged children playing the games, both from the United States and abroad.  </p>
