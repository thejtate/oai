        <p>The Oklahoma Arts Institute at Quartz Mountain is a private, non-profit organization with a mission to provide exceptional multidisciplinary arts experiences that develop individual talent and inspire a lifelong passion for the arts. Since 1977, the Oklahoma Arts Institute has recruited nationally renowned artists to teach a fine arts program for talented Oklahoma youth and a series of continuing education workshops for adults. All programs are held at the Quartz Mountain Arts and Conference Center in Lone Wolf, Oklahoma.</p>
        <p><strong>The Oklahoma Summer Arts Institute is an intensive</strong>, two-week academy in the literary, visual, and performing arts for Oklahoma’s most talented high school students. Students are selected through a statewide competitive audition process, and all accepted students automatically receive a full scholarship to the program, worth over $2,500.</p>
        <p><strong>The Oklahoma Fall Arts Institute</strong> is a series of four-day weekend workshops for educators and adult artists of all ability levels, including true beginners. Instruction is offered in the literary, visual, and performing arts, including workshops tailored to educators, who can receive full scholarships to the program. Over 50,000 Oklahoma public school children are reached each year through their teachers’ attendance at the Fall Institute.</p>
        <p>The Oklahoma Arts Institute at Quartz Mountain is funded through a unique public/private partnership. Major funding is provided by private donors secured by the Oklahoma Arts Institute, with matching funds from the Oklahoma State Department of Education and additional support from the Oklahoma Arts Council and the Oklahoma Tourism and Recreation Department. </p>
