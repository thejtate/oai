        <div class="item-content">
          <div class="item-hd color-l"><h3>Audition Tips</h3></div>
          <div class="item-bd">
            <ul>
              <li>Dance auditions occur after the long winter break. Please take as many technique classes as possible during the break so the adjudicators can see you at your best.</li>
              <li>Arrive at the audition early so you may become familiar with the studio, check-in, prepare, and warm-up.</li>
              <li>Dancewear requirements are specified in the audition materials. Plan your dancewear well in advance of the audition. Wear it for a technique class prior to the audition to be certain it is comfortable and flattering. You want to present a neat, professional appearance.</li>
              <li>Before the audition, please tell the instructor about any recent or chronic injuries so he or she can help you take appropriate precautions.</li>
              <li>During the audition, if you don’t understand a combination or direction, raise your hand and ask the instructor to repeat the information.</li>
              <li>Approach the audition as an opportunity to take a class from a new teacher. The teacher and the adjudicators realize you may be a bit nervous. They want to see your movement and performance qualities, as well as your technique, so enjoy the experience!</li>
            </ul>
          </div>
        </div>
        <div class="item-content">
          <div class="item-hd color-l"><h3>Audition Attire (Mandatory)</h3></div>
          <div class="item-bd">
            <h6>Females</h6>
            <p>Black leotards, pink tights, pink slippers, and pointe shoes. Hair must be pulled back and secured off the face. (You will wear slippers for the majority of the audition, then will be asked to put on pointe shoes for the final part of the audition.)</p>
            <h6>Males</h6>
            <p>White t-shirts, black tights, and black shoes. Long hair must be pulled back and secured off the face.</p>
