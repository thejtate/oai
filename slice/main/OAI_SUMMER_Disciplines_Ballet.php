            <p style="font-size: 18px;"><strong>Ballet instructional sessions include five hours a day of advanced ballet technique, ballet variations, pointe class, and pas de deux class (if enrollment permits).</strong> Students rehearse and perform in variations of ballet repertoire at lecture/demonstrations and/or workshop presentations. Students rehearse and perform in a final project. 20 students are accepted.</p>
