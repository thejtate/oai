          <div class="item-hd color-l"><h3>Requirements for Applicants</h3></div>
          <div class="item-bd">
            <h6>PREREQUISITES</h6>
            <p>
              • A minimum of three (3) years of formal ballet training<br/>
              • Applicants must currently participate in at least three (3) ballet classes per week. <br/>
              • Female applicants must have a minimum of two (2) years of pointe experience and must currently participate in at least two (2) pointe classes per week.
            </p>
            <hr>
            <p><strong>1. Complete an online application and schedule your audition</strong> no later than noon the weekday before your desired audition date. See dates in right side bar or
              <a href="#">view full audition schedule here</a>. Audition spaces fill quickly, so schedule as soon as possible.</p>
            <p><strong>2. Bring a government-issued I.D.</strong> or birth certificate to your audition for age verification purposes.</p>
            <p><strong>3. Read below guidelines</strong> to prepare your audition.</p>
            <p><strong>4. Arrive at your audition</strong> at least 15 minutes early to check in and warm. <strong>The audition is a master class consisting of barre and floor work.</strong> </p>
