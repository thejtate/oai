          <div class="item-hd color-c"><h4>Who can apply?</h4></div>
          <div class="item-bd">
            <ul>
              <li>You must be enrolled in an Oklahoma public school, private school, or homeschool program.</li>
              <li>To audition for this year’s program, you must be at least 15 years old by September 1, 2017. (i.e. It’s okay if you’re 14 during the Summer Institute, but you must turn 15 by September 1, 2017 to apply.) Graduating seniors up to age 19 are also eligible. This means the vast majority of current 9th – 12th graders can apply.</li>
            </ul>
          </div>
        </div>
        <div class="item-content">
          <div class="item-hd color-c"><h4>Can I apply for more than one art form (discipline)?</h4></div>
          <div class="item-bd">
            <ul>
              <li>Yes. You can apply for up to four art forms/ disciplines.</li>
              <li>If you are talented in more than one area, consider applying for more than one discipline. It will increase your chances of being accepted to OSAI.</li>
              <li>When you apply for OSAI, you will rank your disciplines in order of preference. If your audition score qualifies you for more than one discipline, your ranking will determine which discipline you will study at OSAI.</li>
              <li>We have lowered the cost to apply for multiple disciplines! The non-refundable application fee is $20 for one discipline and $15 for each discipline after that.</li>
            </ul>
          </div>
        </div>
        <div class="item-content">
          <div class="item-hd color-c"><h4>When is the application deadline?</h4></div>
          <div class="item-bd">
            <ul>
              <li>You must complete your online application and schedule your audition no later than NOON the weekday before your audition. For example, if you wish to audition on Saturday, January 23, you must have completed your online scheduling by noon on Friday, January 22.</li>
              <li>You must bring a government-issued I.D. or a birth certificate with you to your audition for age verification purposes.</li>
            </ul>
          </div>
        </div>
        <div class="item-content">
          <div class="item-hd color-c"><h4>If I audition in a small town instead of a large city, will I have a better chance of being accepted?</h4></div>
          <div class="item-bd">
            <p>No. Auditions are judged on a statewide basis, not by audition location. Acceptance is based on audition scores, not on demographics.
              <a href="#">You can read more about our adjudication procedures here</a>.</p>
          </div>
        </div>
        <div class="item-content">
          <div class="item-hd color-c"><h4>When will I found out if I’ve been accepted?</h4></div>
          <div class="item-bd">
            <p>All applicants, whether accepted, not accepted, or accepted as an alternate, will be notified by email in April.</p>
          </div>
        </div>
        <div class="item-content">
          <div class="item-hd color-c"><h4>How much does it cost to attend?</h4></div>
          <div class="item-bd">
            <ul>
              <li>Every accepted student receives a full scholarship to attend OSAI.</li>
              <li>Your scholarship includes tuition, room and board, equipment, facilities, operating costs, and full-time security, nursing, counseling, and administrative staffs.</li>
              <li>In addition to the $20 application fee, attending students must pay a $250 non-refundable processing fee. Some disciplines, such as photography, require students to provide supplies.</li>
              <li><i>Students demonstrating financial need may be eligible for assistance through the OAI Angel Fund. Please do NOT let financial concerns deter you from applying. If you have questions about costs or assistance, call (405) 605-7500 or email
                  <a href="mailto:oai@oaiquartz.org">oai@oaiquartz.org</a>. Accepted students with financial need may access the Angel Fund application
                  <a href="#">here</a>.</i></li>
              <li>Scholarships are provided by the Oklahoma State Department of Education and matched by private donations secured by the Oklahoma Arts Institute.</li>
              <li>Additional program funding is provided by the Oklahoma Arts Council.</li>
            </ul>
          </div>
        </div>
        <div class="item-content">
          <div class="item-hd color-c"><h4>Can I arrive at the Summer Arts Institute late or leave early?</h4></div>
          <div class="item-bd">
            <p>No. Students must check in at the Quartz Mountain Arts and Conference Center in Lone Wolf, OK, on June 10, 2017 and remain until the end of the Closing Ceremonies on June 25, 2017. Students are NOT allowed to leave the camp during the program, except during a few designated hours on Sunday, June 18.</p>
          </div>
