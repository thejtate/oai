        <div class="item-inner-bd">
          <p><strong> OSAI discipline: Orchestra</strong></p>

          <p><strong>Dates of attendance: 1977</strong></p>

          <p>
            <strong>YOLANDA KONDONASSIS</strong> is one of the world’s premier solo harpists and is widely
            regarded as today’s
            most recorded classical harpist. Hailed as “a brilliant and expressive player” (Dallas Morning
            News), she has performed around the globe as a concerto soloist and in recital, appearing with
            numerous major orchestras such as The New York Philharmonic, English Chamber Orchestra, and Hong
            Kong Philharmonic, to name a few. Also a published author, speaker, professor of harp and
            environmental activist, she weaves her many passions into a vibrant and multi-faceted career.
          </p>

          <p>
            With hundreds of thousands of albums sold worldwide, Yolanda’s extensive discography includes
            nineteen titles and her 2008 release, Air (Telarc), was nominated for a Grammy Award. Her next
            album, celebrating Ginastera's Centennial, features the Ginastera Harp Concerto and will be released
            in October 2016 on Oberlin Music. Her many albums have earned universal critical praise as she
            continues to be a pioneering force in the harp world, striving to make her instrument more
            accessible to audiences and to push the boundaries of what listeners expect of the harp.
          </p>

          <p>
            Yolanda has been featured on CNN and PBS as well as Sirius/XM Radio’s Symphony Hall, NPR’s All
            Things Considered and Tiny Desk Concerts, St. Paul Sunday Morning, and Performance Today. In
            addition to her active solo, chamber music and recording schedule, Yolanda heads the harp
            departments at the Oberlin Conservatory of Music and The Cleveland Institute of Music, and presents
            master classes around the world. Yolanda attended the Oklahoma Summer Arts Institute in its first
            year.
          </p>
