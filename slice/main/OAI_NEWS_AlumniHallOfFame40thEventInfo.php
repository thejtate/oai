          <p>
            Tim was born and raised in Tulsa and attended the Summer Arts Institute for three years in creative
            writing.
            He is a graduate of Brown University and the recipient of the John Rowe Workman Award for Excellence
            in
            Classical Studies. After Brown he attended the Julliard Theater Center’s four-year actor training
            program.
            He
            resides in New York City with his wife and three sons.   
