        <div class="item-inner-hd color-g"><h4>Timothy Long <span>PIANIST & CONDUCTOR</span></h4></div>
        <div class="item-inner-bd">
          <p><strong> OSAI discipline: Orchestra</strong></p>

          <p><strong>Dates of attendance: 1983-1986</strong></p>

          <p>
            <strong>TIMOTHY LONG</strong> has enjoyed a multi-faceted career as a pianist, conductor, opera coach
            and
            educator. His
            early training as a pianist and violinist led to his work with singers and operatic engagements that
            include companies such as the Boston Lyric Opera, Wolf Trap Opera, The Juilliard School, New York City
            Opera, Opera Theatre of Saint Louis, Théâtre Municipal de Castres, and summer festivals such as the
            Aspen Music Festival and School and the Moab Music Festival. After working on Thomas Adès’ operatic
            tour
            de force, Powder Her Face, Tim was named assistant conductor at the Brooklyn Philharmonic. He also led
            the Stony Brook Symphony Orchestra and the Oregon Bach Festival Orchestra.
          </p>

          <p>
            Off-Broadway, he was music director, conductor and featured pianist for The Music Teacher, an “opera
            within a play” by Wallace Shawn and Allen Shawn. Bridge Records released a recording of this unique
            show
            with Parker Posey and Wallace Shawn in the leading roles. Most recently, he completed a recording of
            two
            Dominick Argento song cycles, The Andrée Expedition and From the Diary of Virginia Woolf, with
            celebrated baritone, Brian Mulligan, to be released this spring. Tim is now a tenured associate
            professor at SUNY-Stony Brook, artist faculty member of the Aspen Music Festival and School, and music
            director and co-founder of Voices of Hope.
          </p>

          <p>
            While growing up in Holdenville, Oklahoma, Tim attended the Oklahoma Summer Arts Institute for four
            consecutive summers, returning this past summer as the orchestra conductor. He studied piano and
            violin
            at Oklahoma City University and then piano performance at the Eastman School of Music. Tim is one half
            Muscogee Creek and one half Choctaw and is from the Wolf clan.
          </p>
