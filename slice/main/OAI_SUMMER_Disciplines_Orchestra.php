                  <a href="#">> Trombone</a> <br/>
                  <a href="#">> Trumpet</a> <br/>
                  <a href="#">> Tuba</a> <br/>
                  <a href="#">> Viola</a> <br/>
                  <a href="#">> Violin</a> <br/>
                </p>
              </li>
              <li>To request a hard copy of the excerpt, please email <a
                  href="mailto:oai@oaiquartz.org">oai@oaiquartz.org</a> or call 405.605.7500.</li>
            </ul>
            <p><strong>4. Sight-Reading</strong></p>
            <ul class="rteindent2">
              <li>Each applicant will have 45 seconds to review the sight-reading before performing the music.
              </li>
              <li>Percussionists will sight-read on snare, marimba, and timpani. </li>
              <li>Harpists will not sight-read.</li>
            </ul>
            <p><strong>PERCUSSIONISTS PLEASE NOTE:</strong> All percussion instruments will be provided, but please bring your own sticks, mallets, and a tuning fork or pitch pipe to tune the timpani. (These items will NOT be provided at the audition.) Electronic tuners are NOT allowed. You may bring your own snare drum if desired.</p>
