            <p style="font-size: 18px;"><strong>The Oklahoma Summer Arts Institute Orchestra is a full symphony orchestra.</strong> All participants rehearse and perform with professional orchestral musicians under the direction of internationally recognized conductors. The daily schedule includes full orchestra rehearsals as well as individual and sectional coaching. Repertoire consists of standard and contemporary symphonic literature. Students may also participate in chamber music and ensemble work. The orchestra performs two concerts during the Institute, and the chamber music ensembles will have one evening performance.</p>
            <p style="font-size: 18px;"><strong><i>Students may NOT audition on the following instruments: English horn, piccolo, saxophone, contrabassoon, bass clarinet, E-flat clarinet, bass trombone, or euphonium (baritone).</i></strong></p>
