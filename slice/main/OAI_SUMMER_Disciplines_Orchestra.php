          <div class="item-hd color-d"><h3>Audition Guidelines</h3></div>
          <div class="item-bd">
            <p><strong>The audition consists of 4 components.</strong></p>
            <p><strong>1. Scale</strong></p>
            <ul class="rteindent2">
              <li>Play the following scale at a tempo thatâs comfortable for you.
              <p>
                â Violin, Viola, and Cello: 3-octave major scale<br/>
                â Double bass: 2-octave major scale<br/>
                â Winds and brass: 2-octave chromatic scale<br/>
                â Percussion and Harp: scales not required. Please see instrument-specific guidelines below.
              </p>
              </li>
            </ul>
            <p><strong>2. Solo Piece</strong></p>
            <ul class="rteindent2">
              <li>All applicants must perform a work, maximum of one minute, which represents overall musicianship. The
                work does NOT have to be memorized, but should demonstrate your ability to play lyrically and
                technically. The work can be a solo, etude, All-State etude, or excerpt from an orchestral work. NO
                accompanists.
              </li>
              <li>Applicants must provide a copy of the work performed for the audition.</li>
              <li>Percussionists must prepare a two- or four-mallet solo that can be performed on a marimba. A variety of repertoire is acceptable and applicants should select a piece they are comfortable playing.</li>
            </ul>
            <p><strong>3. Instrument-Specific Excerpt</strong></p>
            <ul class="rteindent2">
              <li>All applicants must perform the excerpt selected for their instrument. Select your instrument title below to download a copy of the required excerpt.
                <p class="rteindent2">
                  <a href="#">> Bassoon</a> <br/>
                  <a href="#">> Cello</a> <br/>
                  <a href="#">> Clarinet</a> <br/>
                  <a href="#">> Double Bass</a> <br/>
