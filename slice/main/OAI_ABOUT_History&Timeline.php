  <div class="item-hd color-g"><h3>Brief History</h3></div>
  <div class="item-bd">

    <p><strong>The Oklahoma Arts Institute, a vision of then-Governor David Boren, was founded in response to
        concerned parents requesting quality arts education for Oklahoma’s talented young people</strong>. In
      1977, organizers launched a three-day pilot program that provided 100 Oklahoma high school students with
      intensive arts training in five disciplines. Directed by Mary Frates, the first Oklahoma Summer Arts
      Institute at Camp Egan near Tahlequah was a resounding success. The tradition of hiring the nation’s top
      artists to teach the program began that first year, with prima ballerina Maria Tallchief, conductor Judith
      Somogi, and former U.S. Poet Laureate Donald Hall on the faculty.</p>

    <div class="collapsed-content">
      <p>In its second year, the Summer Institute was relocated to the Quartz Mountain State Park and was expanded to
        a two-week program. Today, the Summer Institute serves almost 300 high school students studying in the
        fields of acting, ballet, choral music, creative writing, drawing/painting, film/video, modern dance,
        orchestral music, and photography. These students are accepted through a rigorous statewide audition process
        aimed at identifying Oklahoma’s most talented young artists. All accepted students automatically receive a
        full scholarship for attendance, thereby ensuring that all students can attend, regardless of financial
        means.</p>

      <p>In response to popular demand from Oklahoma teachers, the Oklahoma Arts Institute created the Oklahoma Fall
        Arts Institute in 1983. The series of four-day weekend workshops for adults offers training with nationally
        renowned artists in the literary, visual, and performing arts. Fall Institute workshops continue to offer
        intensive arts education not only for teachers, but for artists of all levels from Oklahoma and beyond.</p>

      <p>In 1991, the Oklahoma legislature passed a joint resolution designating the Oklahoma Arts Institute
        “Oklahoma’s Official School of the Arts.” The same year, the Oklahoma State Department of Education began
        providing matching scholarships for students to attend the Summer Institute and for Oklahoma public school
        teachers to participate in the Fall Institute.</p>

      <p>1995 was a challenging year for the Institute. A devastating fire destroyed the Quartz Mountain Lodge, the
        library, and some of the student artwork housed in the lodge. The tragic fire was nonetheless an opportunity
        to rebuild Quartz Mountain State Park to suit the Institute’s programs. The Summer Institute returned to a
        new and improved Quartz Mountain in June of 2001. The rebuilt facility includes a 700-seat performance hall,
        a state-of-the-art darkroom, and the Beverly Badger Memorial Library. The Quartz Mountain Arts and
        Conference Center is now owned and operated by the State Regents for Higher Education.</p>

      <p>Today, the Oklahoma Arts Institute continues to recruit the nation’s top artists to teach the programs.
        Actors Jane Alexander and Richard Thomas, Pulitzer Prize winner N. Scott Momaday, Grammy Award winner Leona
        Mitchell, dancers Maria and Marjorie Tallchief, Academy Award winning director Ross Kauffman, and visual
        artists Fritz Scholder, Robert Zakanitch, and Richard Avedon have all taught at Quartz Mountain. Alumni of
        Arts Institute programs, such as Megan Mullally and Tim Blake Nelson, enjoy successful careers in the arts,
        while alumni who stay closer to home enrich the cultural landscape of Oklahoma and the region.</p>
