          <div class="item-hd color-c"><h4>Can I bring a guest/spouse/child?</h4></div>
          <div class="item-bd">
            <p>The Institute office must be notified of all guests. Participants wishing to bring and share a room with a non-participant guest may do so at an additional cost. Participants bringing nonparticipant guests are responsible for paying the $100 private room fee plus the necessary guest fees. All guest fees include lodging & meals for the duration of the weekend. If the guest is an adult (18 or over), the fee is an additional $75; if the guest is a child (ages 3-17), the fee is an additional $50. Please understand that unattended children are prohibited at OFAI, and children may not attend classes. Guests not staying overnight may dine in the lodge dining room at their own expense; meal tickets are available for purchase at the front desk of the lodge. Although guests are never allowed to attend classes during OFAI, they are welcome to attend faculty presentations and yoga classes.</p>
