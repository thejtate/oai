        <div class="item-content">
          <div class="item-hd color-c"><h4>How does the waiting list work?</h4></div>
          <div class="item-bd">
            <p>Each Oklahoma public school educator is eligible to attend one workshop on scholarship. Scholarships are not guaranteed. If no scholarships are available at the time of enrollment, you can choose a different workshop and/or be placed on a waiting list. You can be placed on the waiting lists of up to three workshops through our online enrollment system, and we strongly recommend educators take advantage of the wait list system. Many waitlisted applicants are accepted every year. If no scholarships are available, public educators can attend at the rate paid by private educators. But please be aware public educators who decide to enroll in a class by paying the private educator rate will not be eligible for a scholarship during the current year should one become available. Please see the <a href="#">Tuition and Enrollment</a> page for rates.</p>
          </div>
        </div>
