          <div class="item-hd color-c"><h4>Can I earn professional development hours for attending OFAI?</h4></div>
          <div class="item-bd">
            <p>Yes. Upon completion of each workshop, participants receive a certificate for 18 hours of professional development.</p>
          </div>
