          <div class="item-bd">
            <p>Most people will miss only two days of work. Participants check in from noon-1 pm on Thursday and check out at 10:30 a.m. on Sunday.</p>
            <p>The vast majority of school districts support teachers’ OFAI attendance and provide substitutes. Local educational foundations may assist with attaining substitutes.</p>
