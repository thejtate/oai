          <div class="item-hd color-c"><h4>I'm an Oklahoma public school teacher. How can I receive a full scholarship to OFAI?</h4></div>
          <div class="item-bd">
            <p>Individuals currently employed as Oklahoma public school teachers and administrators automatically receive full scholarships to OFAI upon enrollment, while supplies last. Scholarships include tuition and room and board.</p>
            <p>Scholarships are provided by the Oklahoma State Department of Education and private donors. Additional funding is provided by the Oklahoma Arts Council.</p>
            <p>Educators are responsible for a non-refundable $30 registration fee, and visual arts classes have a $30 studio fee. Scholarships and workshop space are administered on a first come, first served basis, so enroll as soon as possible!</p>
            <p>Unfortunately, substitute teachers do not qualify for scholarships.</p>
