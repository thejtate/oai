          <div class="item-bd">
            <p><strong>
                • Grammar and spelling <br/>
                • Inventiveness <br/>
                • Use of language <br/>
                • Achievement of personal voice <br/>
                • Integration of form and content <br/>
                • Potential
              </strong></p>
