        <div class="item-content">
          <div class="item-bd">
            <p style="font-size: 18px;"><strong>Creative Writing students explore writing techniques through practice, group discussion, and individual critiquing sessions with faculty.</strong> Emphasis is placed on developing a keen awareness of the sound, shape, and connotative possibilities of the written word. <strong><i>Various types of creative writing may be explored, but there will be a strong emphasis on poetry. </i></strong> Applicants should be aware that class discussions are on a collegiate level and may contain mature subject matter. 15-16 students are accepted.
            </p>
            <p style="font-size: 18px;">Writers with strong storytelling abilities and an interest in filmmaking are encouraged to audition for both film & video and creative writing.</p>
          </div>
        </div>
        <div class="item-content">
          <div class="item-hd color-b"><h3>Requirements for Applicants</h3></div>
          <div class="item-bd">
            <p><strong>1. Complete an online application and schedule your audition</strong> no later than noon the weekday before your desired audition date. See dates in right side bar or
              <a href="#">view full audition schedule here</a>. Audition spaces fill quickly, so schedule as soon as possible.</p>
            <p><strong>2. Bring a government-issued I.D.</strong> or birth certificate to your audition for age verification purposes.</p>
            <p><strong>3. Read below guidelines and tips</strong> to prepare for your audition.</p>
            <p><strong>4. Arrive at your audition</strong> at least 15 minutes early to check in. The audition consists of a two-hour writing exercise with provided inspirational pieces.</p>
            <p class="rteindent2">
              <strong>You must bring the following items:</strong>
            </p>
             <ul class="rteindent2">
              <li>A writing sample (see submission requirements below)</li>
              <li>A clipboard or other hard writing surface</li>
              <li>Headphones or earbuds, any kind</li>
              <li>OPTIONAL: a pocket dictionary and/or thesaurus</li>
             </ul>
            <p class="rteindent2">
              Paper and pencils will be provided. Phones or other electronic devices are not allowed to be used during the audition.
            </p>
            <div class="btn-wrap style-a"><a href="#">Click Here to Apply</a></div>
          </div>
        </div>
        <div class="item-content">
          <div class="item-hd color-b"><h3>Writing Submission Guidelines</h3></div>
          <div class="item-bd">
            <ul>
              <li>The writing sample consists of FOUR COPIES of up to FIVE (5) PAGES of your creative writing. Samples
                must be typed and in 12-point font. Pages must be single-sided (print on the front of the page only —
                not front and back). Samples consisting of more than five pages will not be accepted.
              </li>
              <li>All writing submitted must have been written AFTER June 30 of prior year. (Former OSAI students may
                not submit work written at OSAI.)
              </li>
              <li>The writing sample may be any combination of poetry, fiction, personal essay, and/or an excerpt from a
                longer work.
              </li>
              <li>DO NOT write your name on your writing sample. You will be assigned a number at your audition.</li>
            </ul>
          </div>
        </div>
        <div class="item-content">
          <div class="item-hd color-b"><h3>What to Expect at Your Audition</h3></div>
          <div class="item-bd">
            <ul>
              <li>When you arrive at your audition, you will be provided with several images of artwork and a piece of music (via an mp3 player) from which to draw inspiration. You will draw inspiration from one or more of these items to write any form of creative writing you wish (e.g. a poem or story). Do NOT simply describe the artwork/object or try to directly comment on it. Use the artwork/object as a jumping-off point for your imagination.
              </li>
              <li>You will receive approximately 15 minutes of instruction/guidance followed by two hours of writing time. If you complete your writing early, you may leave the audition when you are finished.
              </li>
            </ul>
          </div>
        </div>
        <div class="item-content">
          <div class="item-hd color-b"><h3>Audition Tips</h3></div>
          <div class="item-bd">
            <p><strong>How the creative writing audition works:</strong></p>
            <ul class="rteindent2">
              <li>You may write in any form of creative writing you wish (e.g. a poem or story) with one exception: no screenplays.</li>
              <li>Before you begin writing, you will be provided with pieces of artwork or objects to choose from. Your live writing must be inspired by one of the subjects provided. That means you should use your choice of art/object as a jumping-off point for your imagination.</li>
              <li>For example, you could:
                <p class="rteindent1">
                  – Focus on a painting’s concept or theme<br/>
                  – Write a story about the subject of a piece of art<br/>
                  – Imagine a backstory for the artist or musician<br/>
                  – Write the story the music seems to be telling
                </p>
              </li>
              <li>Dictionaries will be available.</li>
              <li>You will have two hours to write and you may leave when finished.</li>
            </ul>
            <p><strong>We recommend that you:</strong></p>
            <ul class="rteindent2">
              <li>Stick with your choice of inspiration.</li>
              <li>Remember, quality is more important than quantity. It is better to have a finely crafted two-stanza poem or a one-page short story than to produce 10 pages of mediocre writing.</li>
            </ul>
            <p><strong>We do not recommend that you:</strong></p>
            <ul class="rteindent2">
              <li>Simply describe the art/music</li>
              <li>Explain your opinion on the art/music</li>
              <li>Critique the art/music</li>
            </ul>
            <p><strong>Writing samples:</strong></p>
            <ul class="rteindent2">
              <li>Please bring the correct number of pages and copies! Often, students bring samples that are too long and must make quick decisions about which pages to submit. </li>
            </ul>
          </div>
        </div>
