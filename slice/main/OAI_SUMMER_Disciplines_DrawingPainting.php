        <div class="item-content">
          <div class="item-bd">
            <p style="font-size: 18px;"><strong>Drawing & Painting students concentrate on the principles of drawing and painting through studio work, slide lectures, and demonstrations.</strong> Students are introduced to color, imagery, media, and various drawing and painting techniques. The class may also focus on printmaking, 3-D art, and/or mixed media. Students will display their work in public exhibitions. 20 students are accepted.</p>
          </div>
        </div>
        <div class="item-content">
          <div class="item-hd color-e"><h3>Requirements for Applicants</h3></div>
          <div class="item-bd">
            <p><strong>1. Complete an online application and schedule your audition</strong> no later than noon the weekday before your desired audition date. See dates in right side bar or
              <a href="#">view full audition schedule here</a>. Audition spaces fill quickly, so schedule as soon as possible.</p>
            <p><strong>2. Bring a government-issued I.D.</strong> or birth certificate to your audition for age verification purposes.</p>
            <p><strong>3. Choose your portfolio pieces and complete your form.  Read below guidelines and tips</strong> to prepare for your audition.</p>
            <p><strong>4. Arrive at your audition</strong> at least 15 minutes early to check in. The audition consists of ONE 50-minute timed drawing exercise (a still-life) led by an instructor. </p>
            <p class="rteindent2">
              <strong>You must bring the following items:</strong>
            </p>
             <ul class="rteindent2">
              <li>A drawing board, or other hard surface to draw on. An easel is recommended, but not required.</li>
              <li>Your portfolio and five copies of your form (see guidelines below)</li>
              <li>OPTIONAL: blending tool(s)</li>
             </ul>
            <p class="rteindent2">
              <strong>You MUST use OAI-provided supplies. </strong>You will not be allowed to use your own materials. The Oklahoma Arts Institute will provide the following materials for every applicant:
            </p>
            <ul class="rteindent2">
              <li>Compressed charcoal sticks, black</li>
              <li>Newsprint paper (18″ x 24″)</li>
              <li>Gum and kneaded rubber erasers</li>
            </ul>
            <div class="btn-wrap style-a"><a href="#">Click Here to Apply</a></div>
          </div>
        </div>
        <div class="item-content">
          <div class="item-hd color-e"><h3>Portfolio Submission Guidelines</h3></div>
          <div class="item-bd">
            <p><strong>1. You must bring up to three works of art with you to your audition.</strong> </p>
            <ul class="rteindent2">
              <li>The artwork can be any combination of paintings, drawings, mixed media, and/or 3-D artwork (no photography) that you created after June 30 of the prior year. (Former OSAI students cannot submit artwork created at OSAI.) Artwork must be no larger than 18″ x 24″. All of your artwork, including the still-life you will create, will be photographed by a photographer at the audition. The adjudicators will receive the photographs for adjudication at a later date. You will bring all artwork back home with you, including the still-life you create at the audition.</li>
              <li>When selecting artwork, choose whatever best represents your personal strengths and style. You are strongly encouraged to submit portfolio pieces that are based on original ideas and subject matter. (i.e. It is best not to include pieces inspired by images found on the internet or another artist’s work.)</li>
            </ul>
            <p><strong>2. Please</strong> <a href="#">download and complete this form</a> and bring five (5) identical copies with you to your audition.</p>
          </div>
        </div>
        <div class="item-content">
          <div class="item-hd color-e"><h3>What to Expect at Your Audition</h3></div>
          <div class="item-bd">
            <ul>
              <li>The audition consists of one 50-minute timed drawing exercise (a still life) and a portfolio element. The still life will be set up in the middle of the room, and you must draw that particular scene.</li>
              <li>The 50 minutes go by very quickly! Many students are not used to working under a specific time constraint. We suggest you participate in your own mock audition at home or school. Set up a still life and assemble your drawing materials, then set the timer for 50 minutes. You may be surprised at what you weren’t able to accomplish within the time frame. You may need to practice drawing more quickly or set specific benchmarks for yourself within the 50 minutes.</li>
              <li>Large, gestural drawings are preferred over small, intricately detailed drawings.</li>
            </ul>
          </div>
        </div>
        <div class="item-content">
          <div class="item-hd color-e"><h3>Portfolio Tips</h3></div>
          <div class="item-bd">
            <p><strong>Please review the following suggestions for selecting pieces to include in your portfolio.</strong></p>
            <p><strong>Subjects NOT RECOMMENDED for your portfolio:</strong></p>
            <ul class="rteindent2">
              <li>Cartoon/anime characters</li>
              <li>Brands, words, numbers, or symbols</li>
              <li>Drawings of floating, context-free objects
                <p class="rteindent1">
                  – For example: eyeballs, hearts, butterflies
                </p>
              </li>
              <li>Drawings based on images from the internet
                <p class="rteindent1">
                  – Meaning any still image that does not come from your real life.<br/>
                  – For example: celebrities, cityscapes, cars, sunsets
                </p>
              </li>
              <li>Drawings based solely on photos, especially posed photos
                <p class="rteindent1">
                  – For example: photos of friends or pets
                </p>
              </li>
              <li>Subjects you are unable to explore first-hand</li>
            </ul>
            <p><strong>Subjects that have historically been more successful:</strong></p>
            <ul class="rteindent2">
              <li>Landscapes, drawn from life</li>
              <li>Still life drawings</li>
              <li>Abstract work</li>
              <li>Portraits or sketches of people (not based on posed photos)</li>
              <li>Unusual perspectives on or compositions of everyday scenes</li>
              <li>Topics about which you are passionate, and that you have explored first-hand</li>
            </ul>
          </div>
        </div>
        <div class="item-content">
          <div class="item-hd color-e"><h3>Judging Criteria</h3></div>
          <div class="item-bd">
            <p><strong>
                • Live Drawing: Composition & Technique (mark-making, value, and proportion) <br/>
                • Portfolio: Technical Skill, Appropriate Choice of Media (in conveying content), and Personal Style (mature vision and sophistication) <br/>
                • Overall Originality & Creativity <br/>
                • Overall Potential
              </strong></p>
            <p><a href="#">Read general information about our adjudication procedures here.</a></p>
            <p>Download the <a href="#">Conflict of Interest form</a> adjudicators sign.</p>
