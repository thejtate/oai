          <div class="item-bd">
            <h6>PREREQUISITES</h6>
            <ul>
              <li>A minimum of three (3) years of dance training (Specific modern dance experience not required.)</li>
            </ul>
            <hr/>
            <p><strong>1. Complete an online application and schedule your audition</strong> no later than noon the weekday before your desired audition date. See dates in right side bar or
              <a href="#">view full audition schedule here</a>. Audition spaces fill quickly, so schedule as soon as possible.</p>
            <p><strong>2. Bring a government-issued I.D.</strong> or birth certificate to your audition for age verification purposes.</p>
            <p><strong>3. Read below guidelines and tips</strong> to prepare for your audition.</p>
            <p><strong>4. Arrive at your audition</strong> at least 15 minutes early to check in and warm up. <strong>The audition is a master class.</strong> </p>
            <div class="btn-wrap style-a"><a href="#">Click Here to Apply</a></div>
