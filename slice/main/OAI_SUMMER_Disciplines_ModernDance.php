            <p style="font-size: 18px;"><strong>Modern Dance instructional sessions include five hours a day of technique class that develops the body as an expressive instrument through flexibility and mobility and works toward freedom of expression in space.</strong> Students learn composition, improvisation, and rehearse repertoire. Students rehearse and perform in a final project. Approximately 20 students are accepted.</p>
