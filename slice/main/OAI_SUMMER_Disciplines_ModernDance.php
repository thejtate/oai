        <div class="item-content">
          <div class="item-hd color-i"><h3>Audition Tips</h3></div>
          <div class="item-bd">
            <ul>
              <li>Dance auditions occur after the long winter break. Please take as many technique classes as possible
                during the break so the adjudicators can see you at your best.
              </li>
              <li>Arrive at the audition early so you may become familiar with the studio, check-in, prepare, and
                warm-up.
              </li>
              <li>Dancewear requirements are specified in the audition materials. Plan your dancewear well in advance of
                the audition. Wear it for a technique class prior to the audition to be certain it is comfortable and
                flattering. You want to present a neat, professional appearance.
              </li>
              <li>Before the audition, please tell the instructor about any recent or chronic injuries so he or she can
                help you take appropriate precautions.
              </li>
              <li>During the audition, if you don’t understand a combination or direction, raise your hand and ask the
                instructor to repeat the information.
              </li>
              <li>Approach the audition as an opportunity to take a class from a new teacher. The teacher and the adjudicators realize you may be a bit nervous. They want to see your movement and performance qualities, as well as your technique, so enjoy the experience!
              </li>
            </ul>
          </div>
        </div>
